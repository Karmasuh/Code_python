class Moves:
    def __init__(self, name, typing, power, accuracy, pp, is_physical, effect=None):
        self.name = name
        self.typing = typing  # 'Physical' or 'Special'
        self.power = power
        self.accuracy = accuracy
        self.pp = pp
        self.is_physical = is_physical
        self.effect = effect

    def is_available(self):
        return self.pp > 0

    def use_move(self):
        self.pp -= 1

    def calculate_damage(self, attacker, defender):
        # Ensure move can be used
        if not self.is_available():
            return "No PP left for this move!"
        
        # Use move (decrement PP)
        self.use_move()

        # Random factor for damage variability
        damage_variance = random.uniform(0.85, 1.0)

        # Determine if attack hits based on accuracy
        if random.random() > self.accuracy:
            return "The attack missed!"

        # Determine which stat to use for the move
        if self.is_physical:
            attack_stat = attacker.get_stat('atk')
            defense_stat = defender.get_stat('def')
        else:
            attack_stat = attacker.get_stat('spAtk')
            defense_stat = defender.get_stat('spDef')

        # Calculate damage
        damage = (((2 * attacker.level // 5 + 2) * self.power * attack_stat // defense_stat) // 50 + 2) * damage_variance

        return damage

