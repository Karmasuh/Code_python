import random

class Pokemon:
    def __init__(self, name, hp, attack, defense, speed, status=None):
        self.name = name
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.speed = speed
        self.status = status  # e.g., 'paralyzed', 'poisoned', etc.

    def apply_status(self, status):
        self.status = status
        print(f"{self.name} is now {self.status}!")

    def remove_status(self):
        self.status = None
        print(f"{self.name} is no longer affected by status.")

    def take_damage(self, damage):
        self.hp -= damage
        if self.hp < 0:
            self.hp = 0
        print(f"{self.name} took {damage} damage, HP is now {self.hp}.")

class Move:
    def __init__(self, name, damage, secondary_effect=None, chance=0):
        self.name = name
        self.damage = damage
        self.secondary_effect = secondary_effect  # (effect_name, chance)
        self.chance = chance  # Chance of secondary effect happening (0 to 1)

    def use(self, attacker, defender):
        # Primary effect: deal damage
        print(f"{attacker.name} used {self.name}!")
        defender.take_damage(self.damage)

        # Secondary effect: check if it applies
        if self.secondary_effect:
            if random.random() < self.chance:
                effect, _ = self.secondary_effect
                self.apply_secondary_effect(effect, defender)

    def apply_secondary_effect(self, effect, defender):
        if effect == 'poison':
            defender.apply_status('poisoned')
        elif effect == 'burn':
            defender.apply_status('burned')
        elif effect == 'paralyze':
            defender.apply_status('paralyzed')
